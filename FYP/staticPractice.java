
public class staticPractice {

    public static void main(String[] args)
    {
		System.out.println("Floor of 12.6: " + Math.floor(12.6));//returns the largest double value that is greater than or equal to the argument
		System.out.println("Ceil of 12.6: " + Math.ceil(12.6));//returns the smallest double value that is greater than or equal to the argument
		System.out.println("Squaring of 12.6: " + Math.pow(12.6,2));
		System.out.println("Random of 12.6: " + Math.random());
		System.out.println("Rint of 12.6: " + Math.rint(12.6));

    }
}
