
public class Math {

    public static void main(String[] args)
    {
		System.out.println("Floor of 12.6: " + Math.floor(12.6));
		System.out.println("Ceil of 12.6: " + Math.ceil(12.6));
		System.out.println("Squaring of 12.6: " + Math.power(12.6,2));
		System.out.println("Random of 12.6: " + Math.random());
		System.out.println("Rint of 12.6: " + Math.rint(12.6));

    }
}
